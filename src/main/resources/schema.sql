DROP TABLE IF EXISTS USER;
CREATE TABLE USER
(
    ID          	VARCHAR(30)     generated by default as identity,
    LOGIN_ID       	VARCHAR(30)     NOT NULL,
    PASSWORD    	VARCHAR(100)    NOT NULL,
    NICK_NAME		VARCHAR(30)		NOT NULL,
    CREATED_AT		VARCHAR(30)		NOT NULL,
    CREATED_BY		VARCHAR(30)		NOT NULL,
    UPDATED_AT		VARCHAR(30)		NOT NULL,
    UPDATED_BY		VARCHAR(30)		NOT NULL,
    PRIMARY KEY (ID, LOGIN_ID)
);

DROP TABLE IF EXISTS SEARCH_HISTORY;
CREATE TABLE SEARCH_HISTORY
(
	ID          	VARCHAR(50)     generated by default as identity,
    SEARCH_WORD    	VARCHAR(50)     ,
    SEARCH_COUNT    int(5)		    NOT NULL 	DEFAULT 0,
    CREATED_AT		VARCHAR(30)		NOT NULL,
    CREATED_BY		VARCHAR(30)		NOT NULL,
    UPDATED_AT		VARCHAR(30)		NOT NULL,
    UPDATED_BY		VARCHAR(30)		NOT NULL,
    PRIMARY KEY (ID, SEARCH_WORD)
);